package com.grodriguez.melisearchdatasource.model.location;

public class AddressCountry {
    private String id = "";
    private String name = "";

    public AddressCountry() {
    }

    // region GET-SET

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    // endregion

}// End Class
